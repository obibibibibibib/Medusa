package keystrokesmod;

public class kc {
   public double mx;
   public double my;
   public double mz;
   public double nx;
   public double ny;
   public double nz;

   public kc(double x, double y, double z, double x1, double y1, double z1) {
      this.mx = x;
      this.my = y;
      this.mz = z;
      this.nx = x1;
      this.ny = y1;
      this.nz = z1;
   }
}
